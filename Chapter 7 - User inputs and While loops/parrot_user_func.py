# We'll be using the input() function.
# The function pauses the program to wait for the user to enter some text.
# Once Python receives the user's input, it assigns that input to a variable
# to make it convenient to work with.

# Example
message = input("Tell me something, and I will repeat it back to you: ")
# pauses to await user input
print(message)

