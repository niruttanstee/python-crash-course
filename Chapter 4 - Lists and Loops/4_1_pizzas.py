# Create a list of pizzas and then use a for loop to print the name of each pizza.
pizzas = ['pepperoni', 'edna', 'fungi']

# Modify the for loop to print a sentence using the name of the pizza instead of printing just the name of the pizza. e.g. I like "pepperoni" pizza...
