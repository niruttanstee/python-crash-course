standard_list = ['python', 'java', 'javascript', 'css', 'html', 'c++', 'c#']

# 1. Print the message "The first three items in the list are:", then use a slice to print the first three items
# in the list.
print(f"The first three items in the list are: {standard_list[:3]}")

# 2. Print the message "Three items from the middle of the list are:", then use a slice to print the three items 
# from middle of list.
print(f"Three items from the middle of the list are: {standard_list[2:5]}")

# 3. Print the message "The last three items in the list are:", then use a slice to print the last three items.
print(f"The last three items in the list are: {standard_list[-3:]}")